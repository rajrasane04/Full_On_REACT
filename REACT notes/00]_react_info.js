// REACT

// - REACT is a JS library for building UI (user interfaces). 
// - In React, everything revolves around components.
// - A component is a piece of the UI that has its own logic and appearance.
// - A component is a JS function or class that returns a piece of UI.
// - A component can be as small as a button, or as large as an entire page.
// - React components are JS functions that return markup .

