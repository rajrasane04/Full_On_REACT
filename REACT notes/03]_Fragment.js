// Fragment

// - Fragments lets you group a list of children without adding extra nodes to the DOM
// - Fragments can be declared using the `<> </>` syntax (also known as the empty tag syntax)

//---------------------------------------------------------------------------------------------------------------------------------------

// Benefits

// - Avoids Extra DOM Nodes
// - cleaner JSX
// - Improved Semantics

//---------------------------------------------------------------------------------------------------------------------------------------

<>
  <h1>Hello</h1>
  <p>Paragraph</p>
</>

//---------------------------------------------------------------------------------------------------------------------------------------
