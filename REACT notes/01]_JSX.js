// JSX (JavaScript XML)

// - JSX is a JavaScript extension that lets you write HTML-like code in JavaScript files. 
// It's used in React to make UI structure descriptions more readable and convenient.

//---------------------------------------------------------------------------------------------------------------------------------------

// Key Points

// - Syntax extension for JavaScript.
// - Allows HTML-like code in JavaScript files.
// - Enhances readability and convenience in describing UI structure.
// - Used to build user interfaces in React applications.

//---------------------------------------------------------------------------------------------------------------------------------------

// Rules while writing Markup in JSX

// - JSX always Return a single root 
// - Close all the tags
// - camelCase most of the things

//---------------------------------------------------------------------------------------------------------------------------------------

// JSX with Curly Braces

// - JSX allows embedding JavaScript expressions within curly braces `{}`
// - Purpose : Dynamically generate content, compute values, and inject JavaScript logic into JSX.

// for e.g

const name = 'John';
const element = <h1>Hello, {name}!</h1>;

